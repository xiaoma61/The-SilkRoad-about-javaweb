package cn.theslikroad.literature.data;

public class LiteractureData {
	String title;
	String writer;
	String writerword;
	String article;
	String fileroad;
	int id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getWriterword() {
		return writerword;
	}
	public void setWriterword(String writerword) {
		this.writerword = writerword;
	}
	public String getArticle() {
		return article;
	}
	public void setArticle(String article) {
		this.article = article;
	}
	public String getFileroad() {
		return fileroad;
	}
	public void setFileroad(String fileroad) {
		this.fileroad = fileroad;
	}
		

}
